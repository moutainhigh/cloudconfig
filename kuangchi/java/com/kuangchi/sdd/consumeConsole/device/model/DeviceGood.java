package com.kuangchi.sdd.consumeConsole.device.model;

public class DeviceGood {
	private String id;
	private String device_num;
	private String good_or_type_num;
	private String is_type;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDevice_num() {
		return device_num;
	}
	public void setDevice_num(String device_num) {
		this.device_num = device_num;
	}
	public String getGood_or_type_num() {
		return good_or_type_num;
	}
	public void setGood_or_type_num(String good_or_type_num) {
		this.good_or_type_num = good_or_type_num;
	}
	public String getIs_type() {
		return is_type;
	}
	public void setIs_type(String is_type) {
		this.is_type = is_type;
	}
	
}
