package com.kuangchi.sdd.baseConsole.device.model;

public class TimeGroup {
	private Integer id;
	private String times_num;
	private String begin_time;
	private String end_time;
	private String eare_num;
	private String device_num;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getTimes_num() {
		return times_num;
	}
	public void setTimes_num(String times_num) {
		this.times_num = times_num;
	}
	public String getBegin_time() {
		return begin_time;
	}
	public void setBegin_time(String begin_time) {
		this.begin_time = begin_time;
	}
	public String getEnd_time() {
		return end_time;
	}
	public void setEnd_time(String end_time) {
		this.end_time = end_time;
	}
	public String getEare_num() {
		return eare_num;
	}
	public void setEare_num(String eare_num) {
		this.eare_num = eare_num;
	}
	public String getDevice_num() {
		return device_num;
	}
	public void setDevice_num(String device_num) {
		this.device_num = device_num;
	}
	
	
}
