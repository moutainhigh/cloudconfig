package com.kuangchi.sdd.baseConsole.holiday.model;

public class ObjectTime {
	private Integer object_times_id;
	private String object_type;
	private String object_num;
	private String group_num;
	private String create_time;
	private String validity_flag;
	private String create_user;
	private String description;
	
	public Integer getObject_times_id() {
		return object_times_id;
	}
	public void setObject_times_id(Integer object_times_id) {
		this.object_times_id = object_times_id;
	}
	public String getObject_type() {
		return object_type;
	}
	public void setObject_type(String object_type) {
		this.object_type = object_type;
	}
	public String getObject_num() {
		return object_num;
	}
	public void setObject_num(String object_num) {
		this.object_num = object_num;
	}
	public String getGroup_num() {
		return group_num;
	}
	public void setGroup_num(String group_num) {
		this.group_num = group_num;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getValidity_flag() {
		return validity_flag;
	}
	public void setValidity_flag(String validity_flag) {
		this.validity_flag = validity_flag;
	}
	public String getCreate_user() {
		return create_user;
	}
	public void setCreate_user(String create_user) {
		this.create_user = create_user;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}  
	
	
}
