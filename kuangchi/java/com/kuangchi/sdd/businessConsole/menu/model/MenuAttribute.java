package com.kuangchi.sdd.businessConsole.menu.model;

public class MenuAttribute {
	private String url;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
	
	
	
	
	private int cdcc;
	private String lx;

	public int getCdcc() {
		return cdcc;
	}

	public void setCdcc(int cdcc) {
		this.cdcc = cdcc;
	}

	public String getLx() {
		return lx;
	}

	public void setLx(String lx) {
		this.lx = lx;
	}

}
