package com.kuangchi.sdd.businessConsole.employee.model;

public class PaperType {
		Integer papersId;
	    String papersType;
	    String papersValue;
	    String validityFlag;
	    String createUser;
	    String createTime;
	    String describe;
		public Integer getPapersId() {
			return papersId;
		}
		public void setPapersId(Integer papersId) {
			this.papersId = papersId;
		}
		public String getPapersType() {
			return papersType;
		}
		public void setPapersType(String papersType) {
			this.papersType = papersType;
		}
		public String getPapersValue() {
			return papersValue;
		}
		public void setPapersValue(String papersValue) {
			this.papersValue = papersValue;
		}
		public String getValidityFlag() {
			return validityFlag;
		}
		public void setValidityFlag(String validityFlag) {
			this.validityFlag = validityFlag;
		}
		public String getCreateUser() {
			return createUser;
		}
		public void setCreateUser(String createUser) {
			this.createUser = createUser;
		}
		public String getCreateTime() {
			return createTime;
		}
		public void setCreateTime(String createTime) {
			this.createTime = createTime;
		}
		public String getDescribe() {
			return describe;
		}
		public void setDescribe(String describe) {
			this.describe = describe;
		}
	    
		
}
