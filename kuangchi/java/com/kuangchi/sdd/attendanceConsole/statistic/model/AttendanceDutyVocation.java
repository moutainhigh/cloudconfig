package com.kuangchi.sdd.attendanceConsole.statistic.model;

public class AttendanceDutyVocation {
	  Integer id;
	  Integer dutyId;
	  String vocationName;
	  String vocationNum;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getDutyId() {
		return dutyId;
	}
	public void setDutyId(Integer dutyId) {
		this.dutyId = dutyId;
	}
	public String getVocationName() {
		return vocationName;
	}
	public void setVocationName(String vocationName) {
		this.vocationName = vocationName;
	}
	public String getVocationNum() {
		return vocationNum;
	}
	public void setVocationNum(String vocationNum) {
		this.vocationNum = vocationNum;
	}
	  
}
